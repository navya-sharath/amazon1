package navya;

public class Positive_Nagative {
	public static void main(String[] args) {
	int num=0;
	while(true) {
	if(num>0) {
		System.out.println(num+"number is positive");
		break;
	}
	else if(num<0) {
		System.out.println(num+"number is negative");break;
	}
	else {
		System.out.println("number is 0");
		break;
	}

}
}
}